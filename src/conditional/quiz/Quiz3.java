package conditional.quiz;

public class Quiz3 {

	public static void main(String[] args) {
		
		// 문자형 변수 gender 를 선언하고, 문자하나를 대입하세요.
		// 변수의 값이 F면 "여성입니다", 그렇지 않으면 "남성입니다"를 출력하세요.
		
		char gender = 'M';
		
		if (gender == 'F') {
			System.out.println("여자입니다.");
		}else {
			System.out.println("남자입니다.");
		}
		
	}

}
